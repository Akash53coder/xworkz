package com.xworkz.java.collection.set;

import java.util.LinkedHashSet;

public class LinkedHashSetDemo {
	
	public static void main(String[] args) {
		
		LinkedHashSet<Double> lhs = new LinkedHashSet();
		lhs.add(12.0);
		lhs.add(121.9);
		lhs.add(124.8);
		lhs.add(90.0);
		
		System.out.println(lhs);
		
		
	}
}

class IfElseIfLadderDemo{
    public static void main(String args[]){
        Boolean haveAdhar=true;
        Boolean havePan=false;
        Boolean haveVoterId=false;
        if(haveAdhar){
            System.out.println("akash is a citizen of India");
        }
        else if(havePan){
            System.out.println("akash is a citizen of India");
        }
        else if(haveVoterId){
            System.out.println("akash is a citizen of India");
        }else{
            System.out.println("akash is not a citizen of India");
        }
    }
}
